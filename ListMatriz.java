public class ListMatriz {
    int x;
    int y;
    int matriz;

    ListMatriz(int x, int y, int matriz) {
        this.x = x;
        this.y = y;
        this.matriz = matriz;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getMatriz() {
        return matriz;
    }
}
